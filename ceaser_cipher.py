def encrypt(text, shift):
    message = ""
    for letter in text:
        for character in alphabet:
            if character == letter:
                if ((alphabet.index(character)+shift)>25):
                    message += alphabet[((alphabet.index(character)+shift)-25)-1]
                else:
                    message += alphabet[alphabet.index(character)+shift]
    
    return message
    
def decrypt(text, shift):
    message = ""
    for letter in text:
        for character in alphabet:
            if character == letter:
                if ((alphabet.index(character)-shift)<0):
                    message += alphabet[((alphabet.index(character)-shift)+25)+1]
                else:
                    message += alphabet[alphabet.index(character)-shift]
    
    return message

alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n").lower()
text = input("Type your message:\n").lower()
shift = int(input("Type the shift number:\n"))

if direction == "encode":
    print(f"Your encrypted message is: {encrypt(text, shift)}\n")

else:
    print(f"You decrypted message is: {decrypt(text, shift)}\n")
